from PIL import Image


def paint_tiles(input_path, output_path, modify_cells, grid_w=14, grid_h=14, color_index=255):
    """
    按 grid_w x grid_h 网格划分整张图，批量修改多个 (row, col) 区域。

    参数：
        input_path: 输入图片路径 (P 模式 8bit)
        output_path: 输出图片路径
        modify_cells: {(row, col), ...} 要修改的格子坐标集合
        grid_w, grid_h: 每个格子的宽度和高度
        color_index: 调色板索引值 (0~255)
    """
    img = Image.open(input_path)
    if img.mode != "P":
        raise ValueError("图片必须是 P 模式（8-bit 调色板）")

    px = img.load()
    W, H = img.size
    grid_w = W // grid_w
    grid_h = H // grid_h

    for (row, col) in modify_cells:
        # 计算格子范围
        x0 = col * grid_w
        x1 = x0 + grid_w
        y0 = row * grid_h
        y1 = y0 + grid_h

        if x1 > W or y1 > H:
            raise ValueError(f"格子 ({row}, {col}) 超出图片范围")

        # 填充该格子
        for y in range(y0, y1):
            for x in range(x0, x1):
                px[x, y] = color_index

    img.save(output_path)
    print(f"已修改 {len(modify_cells)} 个格子，保存为 {output_path}")


